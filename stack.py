
class Stack(object):
    '''define the stack class '''
    def __init__(self):
        self.items = []

    def isEmpty(self):
        return self.items == []

    def size(self):
        return len(self.items)

    def push(self, item):
        self.items.append(item)

    def pop(self):
        if not self.isEmpty():
            return self.items.pop()
        else:
            raise ValueError("Stack is empty")

    def peek(self):
        return self.items[-1]


